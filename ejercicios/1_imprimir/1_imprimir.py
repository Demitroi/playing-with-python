# La forma mas simple de hacer un print
print("Hola Mundo")
# Imprime: Hola Mundo

# De se le puede indicar el final con el parametro end
print("A", end='.\n')
print("B", end='.\n')
print("C", end='.\n')
# Imprime: A.
# B.
# C.

# Se pueden separar los mensajes por lo que se indique en el parametro end
print("Naranja", "Sandia", "Plátano", sep=". ")
# Imprime: Naranja. Sandia. Plátano
